TupleMappings* SELF::initMappings()
{
    instantiators_.insert(std::make_pair("tuple<rstring title,rstring isbn,rstring author,rstring publisher,float32 price>",&initer));
    TupleMappings * tm = new TupleMappings();
#define MY_OFFSETOF(member, base) \
    ((uintptr_t)&reinterpret_cast<Self*>(base)->member) - (uintptr_t)base
   
    // initialize the mappings 
    
    {
        std::string s("title");
        TypeOffset t(MY_OFFSETOF(title_, tm), 
                     Meta::Type::typeOf<SPL::rstring >(), 
                     &typeid(SPL::rstring));
        addMapping(*tm, t, s, 0);
    }
    
    {
        std::string s("isbn");
        TypeOffset t(MY_OFFSETOF(isbn_, tm), 
                     Meta::Type::typeOf<SPL::rstring >(), 
                     &typeid(SPL::rstring));
        addMapping(*tm, t, s, 1);
    }
    
    {
        std::string s("author");
        TypeOffset t(MY_OFFSETOF(author_, tm), 
                     Meta::Type::typeOf<SPL::rstring >(), 
                     &typeid(SPL::rstring));
        addMapping(*tm, t, s, 2);
    }
    
    {
        std::string s("publisher");
        TypeOffset t(MY_OFFSETOF(publisher_, tm), 
                     Meta::Type::typeOf<SPL::rstring >(), 
                     &typeid(SPL::rstring));
        addMapping(*tm, t, s, 3);
    }
    
    {
        std::string s("price");
        TypeOffset t(MY_OFFSETOF(price_, tm), 
                     Meta::Type::typeOf<SPL::float32 >(), 
                     &typeid(SPL::float32));
        addMapping(*tm, t, s, 4);
    }
    
    return tm;
}

