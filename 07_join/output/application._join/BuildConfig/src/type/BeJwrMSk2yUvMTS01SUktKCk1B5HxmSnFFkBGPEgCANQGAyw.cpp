void SELF::serialize(std::ostream & ostr) const
{
    ostr << '{'
         << "name=" << get_name()  << ","
         << "dept=" << get_dept()  << ","
         << "dept_id=" << get_dept_id()  << ","
         << "dep_name=" << get_dep_name()  
         << '}';
}

void SELF::serializeWithPrecision(std::ostream & ostr) const
{
    ostr << '{';
    SPL::serializeWithPrecision(ostr << "name=", get_name()) << ",";
    SPL::serializeWithPrecision(ostr << "dept=", get_dept()) << ",";
    SPL::serializeWithPrecision(ostr << "dept_id=", get_dept_id()) << ",";
    SPL::serializeWithPrecision(ostr << "dep_name=", get_dep_name()) ;
    ostr << '}';
}

SELF& SELF::clear()
{
    SPL::rstring().swap(get_name());
    get_dept() = 0;
    get_dept_id() = 0;
    SPL::rstring().swap(get_dep_name());

    return *this;
}

