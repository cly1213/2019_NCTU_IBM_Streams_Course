void SELF::serialize(std::ostream & ostr) const
{
    ostr << '{'
         << "op=" << get_op()  << ","
         << "op1=" << get_op1()  << ","
         << "op2=" << get_op2()  << ","
         << "result=" << get_result()  
         << '}';
}

void SELF::serializeWithPrecision(std::ostream & ostr) const
{
    ostr << '{';
    SPL::serializeWithPrecision(ostr << "op=", get_op()) << ",";
    SPL::serializeWithPrecision(ostr << "op1=", get_op1()) << ",";
    SPL::serializeWithPrecision(ostr << "op2=", get_op2()) << ",";
    SPL::serializeWithPrecision(ostr << "result=", get_result()) ;
    ostr << '}';
}

SELF& SELF::clear()
{
    SPL::rstring().swap(get_op());
    get_op1() = 0;
    get_op2() = 0;
    get_result() = 0;

    return *this;
}

